public class ShortMessage {
    private int number;
    private long size;

    public ShortMessage(int number, long size) {
        this.number = number;
        this.size = size;
    }

    public int getNumber() {
        return number;
    }

    public long getSize() {
        return size;
    }


    public void setNumber(int number) {
        this.number = number;
    }

    public void setSize(long size) {
        this.size = size;
    }

}